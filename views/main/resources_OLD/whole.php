			<p>Whole life insurance is a type of permanent life insurance that offers coverage
				for you throughout your life. If you are diligent with paying your premiums, a
				whole life policy will provide you coverage until the last day of your life.</p>

			<p>Among the various types of permanent life insurance policies, whole life insurance
				is the most common. The American Council of Life Insurers (ACLI) reported that out of
				all policies sold to individuals in the US in 2014, 63.7% were whole life policies.</p>

			<p>What are the reasons for the wide popularity of whole life insurance? A clear knowledge and understanding of this insurance policy and its benefits and limitations should provide you with a fair perspective to make your best decision for buying <a href="https://phpstack-223893-690316.cloudwaysapps.com/life-insurance/">life insurance</a>.</p>

			<h2>Key Elements of Whole Life Insurance</h2>

			<p>To comprehend the benefits of a whole life policy, you should first understand its key elements.</p>

			<h3>Permanent Coverage</h3>
			<p>In the case of a <a href="https://phpstack-223893-690316.cloudwaysapps.com/life-insurance/best-policies/">term life insurance policy</a>, the coverage term is fixed—for example 30 years. This means, that the policyholder may outlive the policy duration. If the policy has already expired, then the insurance company will not pay any death benefits to your beneficiaries after you.</p>

			<p>This concern is avoided in whole life insurance, which remains active until your last day, provided you didn’t default on your premium dues. This increases the chances of your beneficiaries receiving the final payout.</p>

			<h3>Fixed Premiums</h3>

			<p>Most permanent life insurance policy premiums are fixed amounts. This means that you pay the same <a href="https://phpstack-223893-690316.cloudwaysapps.com/life-insurance/term/rates/">rate</a> from the beginning of the policy term to the end. The advantages of fixed premiums are:</p>

			<ul>
			 	<li>The premium amount you need to pay in the future is not uncertain or unknown. Therefore, you need not worry whether you can afford future premiums or not.</li>
			 	<li>Budgeting for future premium payments and your personal financial planning becomes easier.</li>
			</ul>

			<h3>Cash Value</h3>

			<p>In majority of whole life policies, a portion of the premium you pay is accrued to a cash value account.</p>

			<ul>
			 	<li>This amount grows into a corpus with time.</li>
			 	<li>You can even withdraw or take a loan against this accumulated savings.</li>
			</ul>

			<p>This additional saving feature of the whole life insurance distinguishes it from <a href="https://phpstack-223893-690316.cloudwaysapps.com/life-insurance/term/">term life insurance</a>.</p>

			<h2>Who Should Buy Whole Life Insurance?</h2>

			<p>The whole life insurance policy suits almost any individual who wants to have lifelong insurance coverage. On the face of it, you might think that whole life insurance suits virtually everyone.</p>

			<p>However, that need not always be the case because under certain circumstances, individuals may not be in need of full life protection. Sometimes people take life insurance for short-term coverage for various reasons.</p>

			<ul>
			 	<li>Consider the case of someone walking down the avenue towards life coverage against student loans or mortgage. Both these loans have fixed terms and a term insurance could be a cheaper and more useful option in this case.</li>
			 	<li>Anyone may want to have coverage for their final expenses. Both term and whole life coverage can be used for this.</li>
			 	<li>In some cases, due to their diverse needs, people choose a combination of both term and whole life insurance.</li>
			</ul>

			<p>Your chosen course of action depends upon your answer to a key question: “How much life insurance do I actually require?”</p>

			<p><strong>Even though the two policies are unrelated, you will find that whole life policy premiums are roughly 10 times that of term life insurance policies for the corresponding periods. Whole life insurance could prove to be less cost-effective option for some people (particularly for older persons).</strong></p>

			<p>For whole life insurance policy, you should ideally have a steady source of income to pay the premiums comfortably. If for some reason, you are unsure of your future income or expenses, you might be better off choosing a term life or a universal insurance policy.</p>

			<h2>Types of Whole Life Insurance Policies</h2>

			<p>Different people may have different goals when they buy a whole life policy.</p>

			<ul>
			 	<li><em><u>Meeting expenses</u></em> – The payouts as well as borrowings taken against the whole life policies may be used to meet both expected and unanticipated expenses.</li>
			 	<li><em><u>Large payouts</u></em> – The substantial death benefits protect your beneficiaries from financial burdens when you are no longer there.</li>
			 	<li><em><u>Access to funds</u></em> – As a saving for your retirement years and emergencies, the whole life corpus acts as a safety net for your immediate and long term future.</li>
			</ul>

			<p>Based on their personal needs and goals, people may choose from the following whole life insurance policies:</p>

			<h3>Universal</h3>

			<p>A part of the premium paid under this policy is accumulated in a cash value account. The policyholder can access this account and borrow from this corpus (take a loan).</p>

			<p>The cash value accumulation helps in two ways:</p>

			<ul>
			 	<li>The amount in this account is tax-free.</li>
			 	<li>You need not repay a loan taken out of this account. The unreturned amount is, under most circumstances, deducted from the final payout value and only the balance is paid to the beneficiaries.</li>
			</ul>

			<p>This cash value account can serve as your contingency access fund and help you during any unforeseen emergency—for instance, a health related emergency. During your later years, you could also use it to supplement your post-retirement income.</p>

			<h3>Variable</h3>

			<p>Here, the cash value portion of the premium is reinvested into various sub-accounts—much like mutual funds. Unlike the Universal whole life policy, where the buildup of premiums lead to fund accumulation in the cash value account, in the variable whole life policy, the accrued value grows at the rate at which each sub-account is growing.</p>

			<p>You can make withdrawals from this cash value accumulation as well. The only difference is that the rate of accumulation will vary for both types of whole life policies.</p>

			<h3>Guaranteed Issue</h3>

			<p>The medical status of the individual to be insured will usually have a direct bearing on the premium he or she needs to pay for their whole life insurance. A person with significant health complications may end up paying higher premiums. In extreme cases, they may be denied insurance.</p>

			<p>Most guaranteed issue polices do not require medical underwriting. This means that the policy premium is independent of the policyholder’s health condition. The medical exams mandatory for other policies are waived for this category of whole life insurance.</p>

			<p>None of the buyers of this insurance, irrespective of age or health condition, are required to undergo a medical exam.</p>

			<h3>Survivorship</h3>

			<p>In most cases, death benefits of a life insurance policy are paid out in the event of death of the insured. Each policy offers coverage to only one persons’ life.</p>

			<p>However, Survivorship insurance is a unique variant of life insurance that covers two individuals—most often a married couple. The beneficiary of such a policy will receive payout only after both the insured die. While even one of the insured survives, the policy continues to be active.</p>

			<h3>Single Premium</h3>

			<p>Most life insurance policy premiums are paid at fixed intervals—monthly, annually etc. But depending on your needs, you may even consider single premium permanent life insurance. For such policies, a substantial sum is collected upfront in lieu of premium. This single premium amount can be quite large, but it spares you:</p>

			<ul>
			 	<li>Any worries about being able to afford premium payments for a prolonged period—literally your entire life.</li>
			 	<li>The risk of missing out on a premium payment.</li>
			</ul>

			<h3>Graded Premium</h3>

			<p>In this type of whole life insurance, the premium amount increases with time. The lower early premiums can match the potentially lower incomes of younger persons. With time, as the policyholder’s income grows, so does the premium amount. This staggered approach makes the whole life insurance more financially prudent and affordable many younger persons.</p>

			<h2>Advantages of Whole Life Insurance</h2>

			<p>The whole life insurance policy is not for a fixed duration like the term life plan. It’s for your whole life.  But do you know that whole life policies are not necessarily limited to the death benefit alone?</p>

			<p>Before selecting a life insurance plan, you should consider all the options available to you. Here is a brief compilation of the potential financial gains you can expect from a whole life insurance policy.</p>

			<h3>1. Guaranteed protection</h3>

			<p>The accumulated cash value of your policy is unaffected by any stock market upheavals. In most cases, the accumulation is through periodic premium accrual that is independent of stock market movements.</p>

			<h3>2. Growth of cash value</h3>

			<p>Your policy’s cash value grows at a fixed rate, independent of stock market climbs and reversals. Therefore, whichever way the market swings, your cash value account will grow at a steady rate.</p>

			<h3>3. Additional income</h3>

			<p>If you wish, you can exchange your whole life coverage for an annuity from which you can earn a steady income for life. This exchange will not draw any taxation. But before you make the swap, consult a registered financial consultant or a tax expert for advice. A professional will be better placed to assess what your actual needs are.</p>

			<h3>4. Potential source of funds</h3>

			<p>During your lifetime, if you need additional funds to meet some important expenses, you can make use of your whole life insurance’s accumulated cash value. You can take a loan from this amount and repay later. But you should remember that:</p>

			<ul>
			 	<li>Interest will accumulate on your payout due to the loan.</li>
			 	<li>Your unpaid loan amount will be adjusted against the death benefits and only the difference will be paid to your beneficiary.</li>
			 	<li>The loan amounts are tax exempt.</li>
			</ul>

			<h3>5. Annual dividends</h3>

			<p>Some insurance companies will share a part of their annual profits with you, as annual dividends paid for the whole life insurance. The dividend amounts are variable and depend upon the profits earned. You may use dividends to purchase additional coverage. This could help you to:</p>

			<ul>
			 	<li>Increase your sum assured</li>
			 	<li>Augment the accrued cash value</li>
			</ul>

			<h3>6. Surrendering for cash value</h3>

			<p>Many unforeseen adverse financial situations can befall a policyholder during their lifetime. These may include:</p>

			<ul>
			 	<li>Retrenchment</li>
			 	<li>Losses in business or investment</li>
			 	<li>Chronic illness</li>
			 	<li>Cash crunch</li>
			</ul>

			<p>In these situations, if the policyholder is in urgent need of money, he or she may surrender the whole life policy in return for the cash value accumulated until then. Some charges may be deducted before the accrued amount is paid to the policyholder. Once surrendered, this policy ceases to exist.</p>

			<ul>
			 	<li>You need not pay any further premiums after policy surrender.</li>
			 	<li>No death benefit can be claimed by beneficiaries named in the original policy.</li>
			</ul>

			<h3>7. Retirement savings vehicle</h3>

			<p>Single premium whole life insurance policies can be a solid retirement saving vehicle. It serves three purposes:</p>

			<ul>
			 	<li>Protects their families from financial burdens, in the event of their death</li>
			 	<li>Acts as a contingency financial relief option</li>
			 	<li>Interests and dividends earned are tax-exempt, increasing the amount available for compounding. The corpus grows faster than other savings options due to these reasons.</li>
			</ul>

			<h2>Limitations of Whole Life Insurance</h2>

			<ul>
			 	<li>Whole life insurance has a few complex dimensions. For instance, you can borrow money from your accumulated cash value only after being insured for a fixed duration. This pre-condition is mainly to ensure that some funds accrue before your withdrawal.</li>
			 	<li>Your policy will lapse if the sum of your outstanding loan and its accumulated interest amount exceeds the cash value of your policy. So, be very careful when you take out any loan against the accumulated amount.</li>
			 	<li>When you take a whole life policy, you commit to make regular premium payments for a lifetime. If you miss this obligation, your policy savings may be penalized. Prolonged default may lead to policy being canceled.</li>
			 	<li>The premium amount payable under the whole life insurance is higher than that of term life. Even though during the initial years premiums are costlier, whole life insurance can be cost-effective, if obtained early in life.</li>
			</ul>

			<p>The main objective of any life insurance policy is to protect your loved ones from financial distress after you are no longer there. Both term life and whole life accomplish this. Some people may prefer a combination of both. Before you decide which one to choose, make sure that you know:</p>

			<ul>
			 	<li>Benefits and limitations of both</li>
			 	<li>Key features of both</li>
			 	<li>How each will help you and your family</li>
			</ul>

			<h2>Term Life Insurance vs. Whole Life Insurance</h2>

			<h3>Salient Features of Term Life Insurance</h3>

			<ul>
			 	<li>Payout only in case of death.</li>
			 	<li>No benefit if your policy term expires before your demise.</li>
			 	<li>Easy on the pocket and uncomplicated to purchase.</li>
			 	<li>Policy valid for fixed term—for instance, 5, 10, 15, 30 years, etc.</li>
			 	<li>Premium increases with age; especially high for persons above 50.</li>
			 	<li>You have to renew the policy if you need coverage beyond its term.</li>
			 	<li>Could serve as a provisional protection to augment permanent life insurance coverage.</li>
			 	<li>Convertible into whole life insurance.</li>
			</ul>

			<h3>Salient Features of Whole Life Insurance</h3>

			<ul>
			 	<li>Lifetime coverage.</li>
			 	<li>Dual benefit of sum assured in case of death as well as cash value from premium accumulation.</li>
			 	<li>Medical clearance mandatory, in most cases.</li>
			 	<li>If purchased without medicals, cost even higher.</li>
			 	<li>Requires 12 – 15 years for cash value to build.</li>
			 	<li>A good estate planning instrument.</li>
			 	<li>Cash value varies with the changes in value of return on investment of its underlying asset.</li>
			 	<li>You can withdraw / borrow a part of the accumulated cash value, while the policy is in force.</li>
			 	<li>Premiums are higher than that of term life insurance, especially during the initial years. But staying invested for decades can offset this cost and make it more economical.</li>
			</ul>

			<h2>Which is better: Term Life or Whole Life?</h2>

			<p>How do you choose between the two? There are many variables to consider. You need to consider multiple aspects of your life for this decision and having an insurance agent walk you through the process is recommended. Before you make the choice, you must consider the following factors:</p>

			<ul>
			 	<li>Your present age.</li>
			 	<li>Your present health.</li>
			 	<li>Your family’s financial needs.</li>
			 	<li>Estimated cost of <a href="https://phpstack-223893-690316.cloudwaysapps.com/life-insurance/burial-insurance/">funeral</a> and similar expenses.</li>
			 	<li>Your children’s age.</li>
			 	<li>Expenses in case of serious long-term ailments.</li>
			 	<li>Your outstanding loans and <a href="https://phpstack-223893-690316.cloudwaysapps.com/life-insurance/mortgage/">mortgage</a> amounts.</li>
			 	<li>Your retirement and post-retirement plans.</li>
			 	<li>Your family’s expected future financial needs—for example children’s’ college education.</li>
			 	<li>Do you need additional savings for your retirement?</li>
			 	<li>Your views on setting up a estate and how taxation will impact such an estate.</li>
			 	<li>Will you be setting up a trust as part of your will?</li>
			 	<li>Your views in donating life insurance proceeds.</li>
			 	<li>Your opinion about perpetual term life premiums that offer no payback except in death.</li>
			</ul>

			<p>For example, if your age is 32, your family includes children in kindergarten and you are the only wage earner in the family, a term life insurance that protects your family’s financial needs may suit you best.</p>

			<p>In the event of your premature demise, what kind of financial safety net would your family need? To know this, you should consider the following expenses:</p>

			<ul>
			 	<li>Annual living costs of your family</li>
			 	<li>Mortgage payments</li>
			 	<li>Other outstanding debts</li>
			 	<li>Children’s long term education expenses</li>
			</ul>

			<p>You may want the duration of your policy to match with the time your children might take to complete college. In such case, term life insurance may be the right choice.</p>

			<p>On top of this, you could choose a whole life insurance policy with the same sum assured. With this insurance:</p>

			<ul>
			 	<li>All your family’s financial worries would be taken care of, just as in the case of term life.</li>
			 	<li>In addition, the accumulated cash value would be a key benefit.</li>
			 	<li>During your life, the same corpus will serve as your financial safety net for emergencies.</li>
			</ul>

			<p>A few years down the line, you may even choose to convert a part of your term life coverage into whole life, for additional retirement savings. But what if the whole life insurance premiums are proving to be a burden in your current financial situation? All these possibilities require careful consideration along with an evaluation of your financial goals.</p>

			<h2>Converting from Term Life to Whole Life</h2>

			<p>Converting a term life insurance policy into any permanent life insurance, including whole life, is normally permitted. Under what circumstances will this be the right course of action? Some situations in which this conversion may be useful include:</p>

			<ul>
			 	<li>You are already in your fifties or sixties and your term life policy is due to expire.</li>
			 	<li>You want to continue your coverage but due to your age or other factors, term life appears expensive or unavailable.</li>
			 	<li>You plan to set up an estate, but the significant estate taxes bother you.</li>
			 	<li>Your will includes a trust.</li>
			 	<li>You are looking for tax-free investment options.</li>
			</ul>

			<strong>Converting your term life to whole life can help you:</strong>

			<ul>
			 	<li>Enjoy extended coverage</li>
			 	<li>Build a reserve cash corpus to use for future borrowing</li>
			</ul>

			<p>Your needs and objectives will determine how you structure and manage your policy. You may seek advice for an experienced insurance agent in order to make a salient choice which best addresses your situation.</p>

			<h2>Buying both Term Life and Whole Life</h2>

			<p>You can simultaneously own both kinds of life insurance. In most cases, persons who consider this option would already own a whole life policy. Any short-term need for additional coverage could trigger the demand for a supplementary term life insurance. The duration of the term life coverage will match the duration of the additional risk.</p>

			<p>In some instances, you may already own a term insurance policy and are interested in whole life insurance an investment option:</p>

			<ul>
			 	<li>For retirement savings</li>
			 	<li>For estate benefits</li>
			</ul>

			<h2>Cash Value and Death Benefit in Whole Life Insurance</h2>

			<p>The beneficiary receives the sum assured while the insurance company absorbs the cash value.</p>

			<ul>
			 	<li>Whole life insurance guarantees death benefit, irrespective of when you die, because other than in extraordinary circumstances, the policy remains active for the duration of your life. But term life benefits are payable for a fixed duration only.</li>
			 	<li>Whole life has an additional benefit of accumulated cash that you can withdraw or take a loan from.</li>
			 	<li>If you withdraw, the death benefit will be reduced by that sum.</li>
			 	<li>Your withdrawal is taxable at your regular rate of income tax.</li>
			 	<li>Once your policy is “paid up”—i.e. your cash value has accumulated enough to allow loan, withdrawal etc.—you can use it to pay your whole life insurance premiums.</li>
			</ul>

			<p>When you tap into the cash value, take care to ensure that the death benefit is not depleted too much. Also, be careful to keep your tax burden within limits while withdrawing.</p>

			<p>Some experts have a counter-argument that leaving too much of a corpus is ill-advised because only the insurance company gets to use it. Having a surplus cash accumulation during your younger days makes good economic sense. The fund acts as a reserve for unanticipated expenses.</p>

			<p>But in later years, if you find your policy has plenty of cash, you consider talking to your insurer for a greater face value amount, citing the cash value. In this manner, you can make sure that:</p>

			<ul>
			 	<li>The cash value doesn’t run to waste. Otherwise, the insurance company will only benefit from the accumulated sum set aside from the premiums you paid.</li>
			 	<li>Your beneficiary will receive a larger payout.</li>
			</ul>

			<p>Seek help from an experienced life insurance agent to determine your best options.</p>

			<h2>Important Riders to Consider</h2>

			<p>While buying your whole life insurance policy you might wish to incorporate the following riders.</p>

			<h3>Waiver of Premium Rider</h3>

			<p>In the event of your being unable to work due to a permanent disability or illness, your premiums may be waived. This is applicable for instances such as:</p>

			<ul>
			 	<li>Amputation</li>
			 	<li>Paralysis (full or partial)</li>
			 	<li>Spinal injuries</li>
			 	<li>Loss of eyesight</li>
			</ul>

			<p>The level of disability that triggers this rider varies across insurance companies. But, having the rider can ensure that your policy doesn’t lapse and your family receives the sum assured someday.</p>

			<p>Imagine a situation where you lost a limb and were incapacitated for months. You would have:</p>

			<ul>
			 	<li>Large unpaid medical bills due to the emergency</li>
			 	<li>All regular living expenses to be met but no income to bear these expenses</li>
			 	<li>Mortgage and other debts that require regular payback</li>
			</ul>


			<p>You have to be able to handle these expenses despite being unemployed. In this type of situation, the premium waiver would be most convenient. That is one commitment you can strike off your list.</p>

			<h3>Critical Illness and Terminal Illness Riders</h3>

			<p>Moreover, another name for this is accelerated death benefit rider, which gives you the benefit of receiving a portion of your policy’s death benefit, upon being diagnosed with a terminal illness. In most cases, this rider may be free or very economical because its terms of applicability can be very stringent.</p>

			<ul>
			 	<li>The percentage of benefit paid in advance can be below 50%.</li>
			 	<li>Definition of terminal illness varies from one policy to another.</li>
			 	<li>Benefits paid along with interest could be taken out of the face value of the policy.</li>
			</ul>

			<p>Critical care rider is also along the same lines. You can utilize an early partial payout from your death benefit if you are diagnosed with an illness that requires prolonged intensive treatment.</p>

			<p>For instance, if you suffer a stroke because of which you need constant nursing care, you can take the premature partial payout to pay your medical bills.</p>

			<p>Since the decision to take advantage of or forego this benefit remains with you, you can opt-in for this rider if your insurer gives it free of cost. But, if it’s not free, then decide after going through the terms and conditions in detail. Make sure that you understand the conditions under which this rider will apply before going for its paid variant.</p>

			<h2>Tax Consequences of Whole Life Insurance</h2>
			<ul>
			 	<li>The death benefit is exempt from tax. Just as in the case of term life insurance payouts, the lump sum paid to your beneficiaries in the event of your demise is not taxed.</li>
			 	<li>Using prudent measures, you can avail tax-free loans issued out of your death benefit. You can forego repayment as well, if you plan properly. This loan can fund expenses such as:</li>
			 	<li>College education of children</li>
			 	<li>Augmenting retirement income</li>
			</ul>

			<p>Do exercise extreme caution with these loans. The same borrowing when poorly managed can become taxable—often when you need the tax break most. Taxation happens if:</p>

			<ul>
			 	<li>Your policy lapses</li>
			 	<li>You surrender your policy</li>
			</ul>

